
<p>Flea is a lightweight PHP framework.</p>

<h2>Flea is</h2>

<ul>
	<li>Quick to install</li>
	<li>Lightweight</li>
	<li>Good for website without back-office</li>
</ul>

<h2>Flea has</h2>

<ul>
	<li>Cache Management</li>
	<li>GZip compression (CSS, JS, HTML)</li>
	<li>Simple and permissive urls</li>
	<li>Simple templating and content (migration easy)</li>
	<li>Support multi language</li>
	<li>Use CSV to manage pages (not content)</li>
</ul>

<h2>Flea don't have</h2>

<ul>
	<li>Back office</li>
	<li>Helpers and secure SQL management</li>
</ul>
