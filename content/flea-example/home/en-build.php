
<p>Flea is a lightweight PHP framework.</p>


<h2>Flea is</h2>

<ul>
	<li>Quick to install</li>
	<li>Lightweight</li>
	<li>Good for little Websites (<200 pages)</li>
</ul>

<h2>Flea has</h2>

<ul>
	<li>Cache Management</li>
	<li>Simple and permissive urls</li>
	<li>SEO utils</li>
	<li>A simple templating engine</li>
</ul>

<h2>Flea doesn't have</h2>

<ul>
	<li>Back office</li>
	<li>Helpers and secure SQL management</li>
</ul>
